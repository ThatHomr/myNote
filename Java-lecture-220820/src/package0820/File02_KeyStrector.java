package package0820;

// 개발할 때 용이한 키 또는 단축키
//  새 구성 	: ctrl + n
//	실행 		: ctrl + f11
//	파일 저장 	: ctrl + s
//  이름 수정	: f2
//	라인 삭제 	: ctrl + d
//  코드 복사	: ctrl + alt + 상, 하
//	코드 이동	: alt + 상, 하
//	커서 이동
//		좌, 우 			: 문자 하나 단위로 이동
//		ctrl + 좌, 우 	: 한 단어 단위로 이동
//		home		: 커서를 해당 라인 맨 앞으로 이동
//		end			: 커서를 해당 라인 맨 뒤로 이동
//	문자 삭제
//		back space	: 커서 앞(왼쪽)의 문자를 삭제
//		del			: 커서 뒤(오른쪽)의 문자를 삭제
//  들여쓰기 : tab
//  내어쓰기 : shift + tab
//	들여쓰기 정리 : 영역 지정 후 ctrl + i
//  코드 정리 : 영역 지정 후 ctrl + shift + f


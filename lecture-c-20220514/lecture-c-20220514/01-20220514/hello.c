// 프로그래밍 관련 용어
//   - 프로그램
//			: 어떤 문제를 해결하기 위해 컴퓨터한테 주어지는
//			  처리 방법과 순서를 기술한 명령문의 집합체
//   - 명령어
//			: 하나 이상의 표현식이 모여 구성된 코드를 읽어 들이는 단위.

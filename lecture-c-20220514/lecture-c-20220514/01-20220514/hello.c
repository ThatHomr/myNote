// <stdio.h>
//		→ C 표준 라이브러리에서 표준 입출력(Standard Input/Output)과
//			 관련된 기능들이 선언/정의되어 있는 헤더 파일(*.ㅗ)
//		→ 프로그램에서 표준 입출력과 관련된 기능을 사용하려면
//		    <Stdio.h>
//
#include <stdio.h>

